const container = document.getElementById("formContainer")



for (let feature in features){

const questionDiv = document.createElement("div")
questionDiv.className="question"

const title = document.createElement("div")
title.className="question-title"
title.innerText = feature

questionDiv.appendChild(title)



features[feature].values.forEach((v,i)=>{

const optionLabel = document.createElement("label")

const radio = document.createElement("input")

radio.type="radio"
radio.name=feature
radio.value=v

optionLabel.appendChild(radio)

optionLabel.appendChild(
document.createTextNode(" "+features[feature].labels[i])
)

questionDiv.appendChild(optionLabel)

})

container.appendChild(questionDiv)

}



document.getElementById("predictForm").onsubmit = async function(e){

e.preventDefault()

const data = {}

for (let feature in features){

const selected = document.querySelector(`input[name="${feature}"]:checked`)

if(!selected){
alert("Please answer: " + feature)
return
}

data[feature] = Number(selected.value)

}



const response = await fetch("http://127.0.0.1:5000/predict",{

method:"POST",

headers:{
"Content-Type":"application/json"
},

body:JSON.stringify(data)

})



const result = await response.json()



const labels = {

0:"No Depression",
1:"Mild Depression",
2:"Moderate Depression",
3:"Severe Depression"

}



document.getElementById("result").innerHTML=

`Prediction: ${labels[result.prediction]} <br>
Confidence: ${(result.confidence*100).toFixed(1)}%`

}